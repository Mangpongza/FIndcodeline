function getQuestions(letter) {
  const qs = QUESTIONS[letter];
  if (!qs) return null;
  return qs.map(q => ({ q: q.q, options: q.options }));
}

function checkAnswers(letter, answers) {
  const qs = QUESTIONS[letter];
  if (!qs || !answers || answers.length !== qs.length) return null;
  return answers.map((ans, i) => ans === qs[i].answer);
}

